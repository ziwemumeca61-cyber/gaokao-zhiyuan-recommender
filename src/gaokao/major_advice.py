# 本科就业偏弱、通常需深造的方向（用于"是否接受深造"的提醒）
_NEEDS_POSTGRAD_CATS = {"理学", "历史学", "哲学"}
_NEEDS_POSTGRAD_KW = ("生物", "化学", "环境", "材料", "物理学", "数学", "地质", "海洋",
                      "天文", "哲学", "历史")
# 考公考编相对对口较多的方向（关键词/门类）
_GOV_FRIENDLY = ("法学", "汉语", "中文", "会计", "财政", "税", "统计", "计算机",
                 "管理", "经济", "教育", "中文", "网络")


def advice_for(name: str, category: str) -> dict[str, str]:
    """返回某专业的中肯建议（pitfall/truth/fit），精选命中优先，否则按门类兜底。"""
    if name in ADVICE:
        return ADVICE[name]
    for cname, info in ADVICE.items():
        key = cname.split("（")[0]
        if key and (key in name or name in key):
            return info
    return CATEGORY_ADVICE.get(category, CATEGORY_ADVICE["__default__"])


def _needs_postgrad(name: str, category: str) -> bool:
    return category in _NEEDS_POSTGRAD_CATS or any(k in name for k in _NEEDS_POSTGRAD_KW)


def family_notes(name: str, category: str, *, family_economy: str = "",
                 accept_postgrad: bool = True, career_intent: str = "") -> list[str]:
    """结合家庭与发展意向，给出温和的个性化提醒（可能为空）。"""
    notes: list[str] = []
    needs_pg = _needs_postgrad(name, category)
    if needs_pg and not accept_postgrad:
        notes.append("该专业本科直接对口岗位偏少、通常需读研深造，而你选择了"
                     "暂不打算深造，建议谨慎或重新考虑深造规划。")
    if needs_pg and family_economy == "一般":
        notes.append("该专业后续多需深造，家庭经济一般的话，请提前规划读研的时间与成本。")
    if category == "艺术学" and family_economy == "一般":
        notes.append("艺术类在器材/培训/实习上持续投入较高，家庭经济一般请量力而行。")
    if career_intent == "考公考编":
        if any(k in name or k in category for k in _GOV_FRIENDLY):
            notes.append("你倾向考公考编：该方向在公务员/事业编招考中相对对口、岗位较多。")
        else:
            notes.append("你倾向考公考编：该方向对口的招考岗位相对较少，报考前请查岗位表。")
    return notes
